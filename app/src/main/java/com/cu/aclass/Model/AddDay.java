package com.cu.aclass.Model;

public class AddDay {
    String day;
    boolean selected;

    public AddDay() {
        this.day = day;
        this.selected = selected;
    }

    public String getDay() {
        return day;
    }

    public void setDay(String day) {
        this.day = day;
    }

    public boolean isSelected() {
        return selected;
    }

    public void setSelected(boolean selected) {
        this.selected = selected;
    }
}
